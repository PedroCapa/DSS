    private boolean validaDados() {
        boolean vazio = (this.password.getPassword().equals("") || this.email.getText().equals(""));
        if (vazio)
            javax.swing.JOptionPane.showMessageDialog(this, "Campos por preencher", "Dados incorretos", 0);

        return !vazio;
    }
    
